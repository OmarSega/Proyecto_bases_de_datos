        try{
            Connection con = conexion.getConexion();
            Statement st = con.createStatement();
            String query = "INSERT INTO `centro_investigacion` values ( null, '"+ centro.getText() + "',"
                    + " '"+calle.getText()+"', '"+colonia.getText()+"', '"+municipio.getText()+"',"
                    + " '"+estado.getText()+"', '"+pais.getText()+"', '"+telefono.getText()+"')";  
            //String cluster = txtcluster.getText();
            st.execute(query);

            // para utilizar el procedimiento almacenado
            // La clase CallableStament sirve para mandar a llamar procedimientos
            

            con.close();

            JOptionPane.showMessageDialog(null,"Se ha creado exitosamente");
            this.dispose();
            centro.setText("");
            calle.setText("");
            colonia.setText("");
            municipio.setText("");
            estado.setText("");
            pais.setText("");
            telefono.setText("");
        }
        catch(SQLException ex){
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(null,"Ha ocurrido un problema");
            this.dispose();
        }
